class NestLoop 
{
	public static void main(String[] args) 
	{
		System.out.println("Start Excercise");
			for(int i=1; i<=3; i++)
			{
				for(int j=1; j<=3; j++)
				{
					System.out.println("Pushup");
				}
				System.out.println("Take 1 min rest");
			}
				System.out.println("Stop Excercise");
	}
}
