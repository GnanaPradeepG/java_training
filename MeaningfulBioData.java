class MeaningfulBioData
{
	public static void main(String[]args)
	{
		String name="Pradeep";
		int age=21;
		double height=5.6;
		char firstLetter='P';
		System.out.println("My name is "+name);
		System.out.println("I am "+age+" years old");
		System.out.println("My height is "+height);
		System.out.println("The first letter of my name is " +firstLetter);
	}
}